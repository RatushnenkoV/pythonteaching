import random

WORDS = [
    # Существительные
    'солнце', 'облако', 'река', 'лес', 'звезда', 'луна', 'ветер', 'огонь', 'земля', 'море',
    'гора', 'поле', 'небо', 'дождь', 'снег', 'цветок', 'дерево', 'птица', 'рыба', 'кот',
    'волк', 'медведь', 'заяц', 'лиса', 'орел', 'сокол', 'тигр', 'лев', 'слон', 'кит',
    'радуга', 'молния', 'буря', 'роса', 'туман', 'закат', 'рассвет', 'полдень', 'вечер', 'ночь',
    # Прилагательные (в форме существительного)
    'умник', 'смельчак', 'храбрец', 'молодец', 'силач', 'мудрец', 'знаток', 'мастер', 'герой', 'друг',
]


def generate_login():
    """Генерирует уникальный логин: слово + 2 случайных числа"""
    word = random.choice(WORDS)
    numbers = random.randint(10, 99)
    return f"{word}{numbers}"


def generate_unique_login(existing_logins):
    """Генерирует уникальный логин, которого нет в списке существующих"""
    max_attempts = 100
    for _ in range(max_attempts):
        login = generate_login()
        if login not in existing_logins:
            return login
    # Если не удалось за 100 попыток, добавляем 3 цифры
    word = random.choice(WORDS)
    numbers = random.randint(100, 999)
    return f"{word}{numbers}"
